from bblean.fingerprints import make_fake_fingerprints
from bblean import BitBirch


def test_refine() -> None:
    fps = make_fake_fingerprints(
        100, n_features=2048, seed=12620509540149709235, pack=True
    )
    tree = BitBirch(branching_factor=50, threshold=0.3, merge_criterion="diameter")
    tree.fit(fps)
    assignments = tree.get_assignments()
    assert assignments.tolist() == EXPECT_ASSIGNMENTS
    tree.refine_inplace(fps)
    assignments = tree.get_assignments()
    assert assignments.tolist() == EXPECT_ASSIGNMENTS_REFINE


